package clases;

/**
 *
 * @author erick
 */
public class Arma {
     private String uuid;
    private String nombre;
    private String imagen;

    public Arma() {
    }

    public Arma(String uuid, String nombre, String imagen) {
        this.uuid = uuid;
        this.nombre = nombre;
        this.imagen = imagen;
    }

    public String getUuid() {
        return uuid;
    }

    public void setUuid(String uuid) {
        this.uuid = uuid;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getImagen() {
        return imagen;
    }

    public void setImagen(String imagen) {
        this.imagen = imagen;
    }
}
